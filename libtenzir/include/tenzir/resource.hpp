#pragma once

#include <cstdint>
#include <string>

namespace tenzir {

struct resource {
  std::string url;
  uint64_t size;

  template <class Inspector>
  friend auto inspect(Inspector& f, resource& x) {
    return f.object(x)
      .pretty_name("tenzir.resource")
      .fields(f.field("url", x.url), f.field("size", x.size));
  }
};

} // namespace tenzir
